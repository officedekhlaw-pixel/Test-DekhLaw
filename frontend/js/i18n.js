/* ============================================================
   ENGINE — apply translations to DOM
   ============================================================ */
function dlApplyLang(lang) {
  var dict = DL_T[lang] || DL_T['en'];

  /* text content */
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.textContent = dict[key];
  });

  /* innerHTML (allows <br>, <strong> etc) */
  document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-html');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  /* placeholder */
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-ph');
    if (dict[key] !== undefined) el.placeholder = dict[key];
  });

  /* title attribute */
  document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-title');
    if (dict[key] !== undefined) el.title = dict[key];
  });

  /* update <html lang> */
  document.documentElement.lang = lang;

  /* sync active state on all lang pill buttons (desktop + mobile) */
  document.querySelectorAll('.tub-lang-pill, .mob-lang-btn').forEach(function(btn) {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

/* ─── Global setLang — called by buttons ─── */
function setLang(lang) {
  try { localStorage.setItem('dl_lang', lang); } catch(e) {}
  dlApplyLang(lang);

}

/* ─── Auto-apply on DOM ready ─── */
(function() {
  function init() {
    var saved = 'en';
    try { saved = localStorage.getItem('dl_lang') || 'en'; } catch(e) {}
    dlApplyLang(saved);
  }
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
})();
